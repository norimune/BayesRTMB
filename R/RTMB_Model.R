    .simple_jacobian = function(f, x, h = 1e-4) {
      p <- length(x)
      f0 <- f(x)
      res <- matrix(0, nrow = length(f0), ncol = p)
      for (i in 1:p) {
        h_i <- max(abs(x[i]), 1e-4) * h
        x_plus <- x
        x_minus <- x
        x_plus[i] <- x[i] + h_i
        x_minus[i] <- x[i] - h_i
        res[, i] <- (f(x_plus) - f(x_minus)) / (2 * h_i)
      }
      return(res)
    },
