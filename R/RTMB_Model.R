  private = list(
    # --- Common patterns for REML and parameter classification ---
    fix_patterns = c("Intercept", "Intercept_c", "b", "mean", "prob", "beta", "mu", "delta", "diff", "mean_diff"),
    var_patterns = c("sigma", "sd", "rho", "rho_resid", "L_resid", "corr", "CF_corr", "cov", "CF_cov", "phi", "tau", "theta", "alpha", "omega", "kappa", "lambda"),

    # --- Internal helper for index computation ---
    .compute_active_indices = function(target_map = NULL) {
      idx_fix_active <- integer(0)
      factor_levels_seen <- list()
      opt_par_curr <- 1
      
      map_to_use <- if (!is.null(target_map)) target_map else self$map
      
      for (name in names(self$par_list)) {
        p <- self$par_list[[name]]
        L_u <- p$unc_length
        m <- if (!is.null(map_to_use[[name]])) map_to_use[[name]] else factor(1:L_u)
        
        # Identify indices that are not NA (i.e., estimated)
        idx_active_local <- which(!is.na(m))
        if (length(idx_active_local) > 0) {
          # Check for shared levels (identical factors)
          lvl_names <- as.character(m[idx_active_local])
          for (lvl in unique(lvl_names)) {
            pos_in_active <- which(lvl_names == lvl)
            # The first occurrence of this level gets the next free optimization index
            if (!(lvl %in% names(factor_levels_seen))) {
              factor_levels_seen[[lvl]] <- opt_par_curr
              opt_par_curr <- opt_par_curr + 1
            }
            # Add to global unconstrained index list
            pos_last <- sum(self$pl_full$unc_length[seq_len(which(names(self$par_list) == name) - 1)]) + idx_active_local[pos_in_active]
            idx_fix_active <- c(idx_fix_active, pos_last)
          }
        }
      }
      return(list(idx_active = sort(unique(idx_fix_active)), n_opt = opt_par_curr - 1))
    },

    # --- Internal helper for REML parameter classification ---
    .classify_reml_parameters = function(target_vars = NULL) {
      fix_names <- character(0)
      var_names <- character(0)
      
      for (name in names(self$par_list)) {
        is_fix_pattern <- any(sapply(private$fix_patterns, function(v) grepl(paste0("^", v, "($|\\[|\\.|_)"), name)))
        is_var_pattern <- any(sapply(private$var_patterns, function(v) grepl(paste0("^", v, "($|\\[|\\.|_)"), name)))
        
        if (is_fix_pattern && !is_var_pattern) {
          fix_names <- c(fix_names, name)
        } else if (is_var_pattern) {
          var_names <- c(var_names, name)
        } else if (!is.null(target_vars) && name %in% target_vars) {
          fix_names <- c(fix_names, name)
        } else {
          # Default to fixed if unknown
          fix_names <- c(fix_names, name)
        }
      }
      return(list(fixed = fix_names, variance = var_names))
    },

    # --- Internal helper for derived parameter calculation (delta method) ---
    .build_derived_df_table = function(func, base_out, is_generate = FALSE, se_sampling = FALSE, num_samples = 1000, sd_rep = NULL) {
      if (is.null(func) || is.null(base_out) || length(base_out) == 0) return(NULL)
      
      names_vec <- c()
      for (name in names(base_out)) {
        val <- base_out[[name]]
        d_val <- if (is.null(dim(val))) length(val) else dim(val)
        names_def <- self$par_names[[name]]
        names_vec <- c(names_vec, generate_flat_names(name, d_val, names_def))
      }
      
      flat_base <- unlist(base_out, use.names = FALSE)
      n_derived <- length(flat_base)
      se_derived <- rep(NA_real_, n_derived)
      df_derived <- rep(Inf, n_derived)
      
      if (!is.null(sd_rep)) {
        smry_rep_all <- as.data.frame(summary(sd_rep, select = "report"))
        m_idx <- match(names_vec, rownames(smry_rep_all))
        valid_m <- !is.na(m_idx)
        if (any(valid_m)) se_derived[valid_m] <- smry_rep_all$`Std. Error`[m_idx[valid_m]]
        
        # Satterthwaite for derived params if needed
        # (This would require Hessian/Jacobian calculations similar to previous logic)
      }
      
      res_df <- data.frame(
        Estimate = flat_base,
        `Std. Error` = se_derived,
        df = df_derived,
        row.names = names_vec,
        check.names = FALSE
      )
      return(res_df)
    }
